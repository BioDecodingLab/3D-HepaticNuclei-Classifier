# FRIEDMAN + POST HOC WILCOXON + HOLM
# Metric: f1_macro
# Baseline post hoc: SVM +1000


import os
import numpy as np
import pandas as pd
from scipy.stats import friedmanchisquare, wilcoxon, rankdata
from statsmodels.stats.multitest import multipletests
from IPython.display import display


# 1. Upload file

input_csv = "/content/drive/MyDrive/tesis/results/boxplot_metrics_summary_TEST_CORRECT.csv"
output_dir = "/content/drive/MyDrive/tesis/results"

df = pd.read_csv(input_csv)

print("Archivo cargado:")
print(input_csv)
display(df.head())


# 2. Reconstruct experiment labels

label_map = {
    "Logistic Regression": "LogReg",
    "Random Forest": "RF",
    "Support Vector Machine": "SVM",
    "MLP": "MLP"
}

def make_experiment_label(model, augmentation):
    short_model = label_map.get(str(model).strip(), str(model).strip())
    aug = str(augmentation).strip().lower()
    if aug == "no_aug":
        return short_model
    return f"{short_model} +{aug}"

df["model"] = df["model"].astype(str).str.strip()
df["augmentation"] = df["augmentation"].astype(str).str.strip().str.lower()
df["fold"] = pd.to_numeric(df["fold"], errors="coerce")
df["f1_macro"] = pd.to_numeric(df["f1_macro"], errors="coerce")

df["experiment_label"] = [
    make_experiment_label(m, a)
    for m, a in zip(df["model"], df["augmentation"])
]


# 3. Select metric and baseline

metric_col = "f1_macro"
baseline = "SVM +1000"

experiment_order = [
    "LogReg", "LogReg +100", "LogReg +500", "LogReg +1000", "LogReg +2000",
    "RF", "RF +100", "RF +500", "RF +1000", "RF +2000",
    "SVM", "SVM +100", "SVM +500", "SVM +1000", "SVM +2000",
    "MLP", "MLP +100", "MLP +500", "MLP +1000", "MLP +2000"
]

df = df[df["experiment_label"].isin(experiment_order)].copy()
df = df.dropna(subset=["fold", metric_col, "experiment_label"]).copy()

print("\nMétrica seleccionada:", metric_col)
print("Baseline:", baseline)


# 4. Folded paired matrix

pivot_df = df.pivot_table(
    index="fold",
    columns="experiment_label",
    values=metric_col,
    aggfunc="mean"
)

available_cols = [col for col in experiment_order if col in pivot_df.columns]
pivot_df = pivot_df[available_cols]

# Keep only complete folds
pivot_df = pivot_df.dropna(axis=0, how="any")

print("\nMatriz pareada:")
display(pivot_df)

print("Shape:", pivot_df.shape)

if pivot_df.shape[0] < 2:
    raise ValueError("No hay suficientes folds para hacer el análisis.")

if pivot_df.shape[1] < 3:
    raise ValueError("Se necesitan al menos 3 experimentos para Friedman.")

if baseline not in pivot_df.columns:
    raise ValueError(f"No se encontró el baseline '{baseline}'.")


# 5. Descriptive statistics per experiment

summary_df = pd.DataFrame({
    "experiment": pivot_df.columns,
    "mean_f1_macro": pivot_df.mean(axis=0).values,
    "std_f1_macro": pivot_df.std(axis=0, ddof=1).values,
    "median_f1_macro": pivot_df.median(axis=0).values
}).sort_values("mean_f1_macro", ascending=False).reset_index(drop=True)

print("\nResumen descriptivo:")
display(summary_df)


# 6. Friedman

friedman_stat, friedman_p = friedmanchisquare(
    *[pivot_df[col].values for col in pivot_df.columns]
)

# average ranks (lower rank = better)
ranks_per_fold = pivot_df.apply(
    lambda row: rankdata(-row.values, method="average"),
    axis=1,
    result_type="expand"
)
ranks_per_fold.columns = pivot_df.columns

avg_ranks_df = pd.DataFrame({
    "experiment": ranks_per_fold.columns,
    "average_rank": ranks_per_fold.mean(axis=0).values
}).sort_values("average_rank", ascending=True).reset_index(drop=True)

friedman_results = pd.DataFrame([{
    "metric": metric_col,
    "n_folds": pivot_df.shape[0],
    "n_experiments": pivot_df.shape[1],
    "friedman_statistic": friedman_stat,
    "friedman_p_value": friedman_p
}])

print("\nResultado Friedman:")
display(friedman_results)

print("\nRangos promedio Friedman:")
display(avg_ranks_df)


# 7. Post hoc: Wilcoxon baseline vs all

baseline_values = pivot_df[baseline].values
posthoc_rows = []

for exp in pivot_df.columns:
    if exp == baseline:
        continue

    comparison_values = pivot_df[exp].values

    if np.allclose(baseline_values, comparison_values):
        stat = 0.0
        p_value = 1.0
    else:
        try:
            stat, p_value = wilcoxon(
                baseline_values,
                comparison_values,
                zero_method="wilcox",
                alternative="two-sided",
                mode="auto"
            )
        except ValueError:
            stat = np.nan
            p_value = 1.0

    baseline_mean = np.mean(baseline_values)
    comparison_mean = np.mean(comparison_values)
    mean_difference = baseline_mean - comparison_mean

    posthoc_rows.append({
        "baseline": baseline,
        "comparison": exp,
        "baseline_mean": baseline_mean,
        "comparison_mean": comparison_mean,
        "mean_difference": mean_difference,
        "wilcoxon_statistic": stat,
        "p_value_raw": p_value
    })

posthoc_df = pd.DataFrame(posthoc_rows)


# 8. Holm Correction

reject, pvals_corrected, _, _ = multipletests(
    posthoc_df["p_value_raw"].values,
    alpha=0.05,
    method="holm"
)

posthoc_df["p_value_holm"] = pvals_corrected
posthoc_df["reject_h0_holm"] = reject

def interpret(row):
    if row["reject_h0_holm"]:
        if row["mean_difference"] > 0:
            return f"{row['baseline']} mejor"
        elif row["mean_difference"] < 0:
            return f"{row['comparison']} mejor"
        else:
            return "diferencia significativa"
    return "sin diferencia significativa"

posthoc_df["interpretation"] = posthoc_df.apply(interpret, axis=1)

posthoc_df = posthoc_df.sort_values(
    ["p_value_holm", "p_value_raw", "comparison"]
).reset_index(drop=True)

print("\nPost hoc Wilcoxon + Holm:")
display(posthoc_df)


