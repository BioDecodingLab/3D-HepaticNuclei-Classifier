# 2. Boxplot function
def make_pretty_boxplot(data, y_col, y_label, title, output_name):
    plot_df = data[["experiment_label", y_col]].dropna().copy()

    print(f"\nPlotting {y_col}")
    display(plot_df.groupby("experiment_label").size().reset_index(name="n"))

    if plot_df.empty:
        raise ValueError(f"No data available for plotting {y_col}")

    plt.figure(figsize=(20, 8))

    ax = sns.boxplot(
        data=plot_df,
        x="experiment_label",
        y=y_col,
        order=experiment_order,
        width=0.58,
        showcaps=True,
        showfliers=False,
        boxprops=dict(facecolor="#cfe8ff", edgecolor="#1f4e79", linewidth=1.8),
        whiskerprops=dict(color="#1f4e79", linewidth=1.8),
        capprops=dict(color="#1f4e79", linewidth=1.8),
        medianprops=dict(color="#08306b", linewidth=2.5)
    )

    sns.stripplot(
        data=plot_df,
        x="experiment_label",
        y=y_col,
        order=experiment_order,
        color="#0b3d91",
        size=4,
        alpha=0.7,
        jitter=0.16,
        ax=ax
    )

    ax.set_xlabel("Model and augmentation setting", fontsize=13, fontweight="bold")
    ax.set_ylabel(y_label, fontsize=13, fontweight="bold")
    ax.set_title(title, fontsize=15, fontweight="bold", pad=14)

    plt.xticks(rotation=45, ha="right", fontsize=10)
    plt.yticks(fontsize=10)

    ax.grid(axis="y", linestyle="--", alpha=0.25)
    ax.grid(axis="x", visible=False)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    plt.tight_layout()

    save_path = os.path.join(plots_output_folder, output_name)
    plt.savefig(save_path, format="svg", bbox_inches="tight", dpi=300)
    plt.show()
    plt.close()

    print(f"Saved plot: {save_path}")

