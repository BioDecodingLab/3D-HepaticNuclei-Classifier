def count_patches_by_label(root_dir):
    if not os.path.exists(root_dir):
        print("No existe:", root_dir)
        return

    folders = sorted([d for d in os.listdir(root_dir) if d.startswith("label_")])
    print("\nConteo en:", root_dir)
    for d in folders:
        path = os.path.join(root_dir, d)
        n = len([f for f in os.listdir(path) if f.lower().endswith((".tif",".tiff"))])
        print(f"{d}: {n}")

count_patches_by_label(patches_train_dir)
